let numero1 = Number(prompt('Digite um número'));
let numero2 = Number(prompt('Digite outro número'));

alert(`A soma do ${numero1} + ${numero2} = ${numero1 + numero2}`);
alert(`A subtração do ${numero1} - ${numero2} = ${numero1 - numero2}`);
alert(`A divisão do ${numero1} / ${numero2} = ${numero1 / numero2}`);
alert(`A multiplicação do ${numero1} x ${numero2} = ${numero1 * numero2}`)
alert(`O quadrado do ${numero1} é ${numero1 * numero1}`);
alert(`A raiz quadrada do ${numero2} é ${Math.sqrt(numero2)}`);
alert(`O resto da divisão do ${numero1} / ${numero2} = ${numero1 % numero2}`);
alert(`A parte inteira da divisão do ${numero1} / ${numero2} = ${Math.floor(numero1 / numero2)}`)
